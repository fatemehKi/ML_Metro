import numpy as np
import pandas as pd
import matplotlib.pyplot as plt

dataset = pd.read_csv('Social_Network_Ads.csv')
X = dataset.iloc[:,2:4].values
y = dataset.iloc[:,-1].values

#Visualising the dataset                
plt.scatter(X[y==0,0],X[y==0,1], c='red', marker='_')
plt.scatter(X[y==1,0],X[y==1,1], c='green',marker='+')


from sklearn.preprocessing import StandardScaler 
sc_X = StandardScaler()
X = sc_X.fit_transform(X)


from sklearn.model_selection import train_test_split
X_train, X_test, y_train, y_test = train_test_split(X,y,random_state=0)


from sklearn.svm import SVC
from sklearn.neighbors import KNeighborsClassifier

clf1 = SVC(kernel='linear',random_state=0)
clf2 = SVC(kernel='poly',degree=2,random_state=0)
clf3 = SVC(random_state=0) #rbf
clf4 = KNeighborsClassifier(n_neighbors=5)
          
          
clf1.fit(X_train,y_train)
clf2.fit(X_train,y_train)
clf3.fit(X_train,y_train)          
clf4.fit(X_train,y_train)          


clf1.score(X_train,y_train)
clf2.score(X_train,y_train)
clf3.score(X_train,y_train)
clf4.score(X_train,y_train)

clf1.score(X_test,y_test)
clf2.score(X_test,y_test)
clf3.score(X_test,y_test)
clf4.score(X_test,y_test)

# Visualising the Training set results for Linear SVM
from matplotlib.colors import ListedColormap
X_set, y_set = X_train, y_train
X1, X2 = np.meshgrid(np.arange(start = X_set[:, 0].min() - 1, stop = X_set[:, 0].max() + 1, step = 0.01),
                     np.arange(start = X_set[:, 1].min() - 1, stop = X_set[:, 1].max() + 1, step = 0.01))
plt.contourf(X1, X2, clf1.predict(np.array([X1.ravel(), X2.ravel()]).T).reshape(X1.shape),
             alpha = 0.75, cmap = ListedColormap(('red', 'green')))
plt.xlim(X1.min(), X1.max())
plt.ylim(X2.min(), X2.max())
for i, j in enumerate(np.unique(y_set)):
    plt.scatter(X_set[y_set == j, 0], X_set[y_set == j, 1],
                c = ListedColormap(('red', 'green'))(i), label = j)
plt.title('Linear SVM (Training set)')
plt.xlabel('Age')
plt.ylabel('Estimated Salary')
plt.legend()
plt.show()

